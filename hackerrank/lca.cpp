#include <bits/stdc++.h>

using namespace std;

class Node {
    public:
        int data;
        Node *left;
        Node *right;
        Node(int d) {
            data = d;
            left = NULL;
            right = NULL;
        }
};

class Solution {
    public:
  		Node* insert(Node* root, int data) {
            if(root == NULL) {
                return new Node(data);
            } else {
                Node* cur;
                if(data <= root->data) {
                    cur = insert(root->left, data);
                    root->left = cur;
                } else {
                    cur = insert(root->right, data);
                    root->right = cur;
               }

               return root;
           }
        }

/*The tree node has data, left child and right child 
class Node {
    int data;
    Node* left;
    Node* right;
};

*/
  
    Node *lca(Node *root, int v1,int v2) {
        Node *curr = root;
        int sm, bg;
        if(v1 < v2) { sm = v1; bg = v2;}
        else { sm = v2; bg = v1; }

        while(curr != NULL) {
            if((sm <= curr -> data) && (bg >= curr -> data)) return curr;
            if(bg < curr -> data) curr = curr -> left;
            else curr = curr -> right;
        }
        return NULL;
    }
}; //End of Solution

